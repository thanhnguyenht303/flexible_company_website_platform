export function isLanguage(value: unknown): value is Language {
  return typeof value === "string" && languages.includes(value as Language);
}

export function normalizeLanguage(value: unknown): Language {
  return isLanguage(value) ? value : defaultLanguage;
}

export function translate(language: Language, key: string, values?: TranslationValues) {
  const value = getTranslationValue(translations[language], key) ?? getTranslationValue(translations.en, key) ?? key;
  return interpolate(String(value), values);
}

function getTranslationValue(source: unknown, key: string): unknown {
  return key.split(".").reduce<unknown>((current, part) => {
    if (!current || typeof current !== "object" || !(part in current)) return undefined;
    return (current as Record<string, unknown>)[part];
  }, source);
}

function interpolate(value: string, replacements?: TranslationValues) {
  if (!replacements) return value;

  return value.replace(/\{(\w+)\}/g, (match, name) => {
    const replacement = replacements[name];
    return replacement === undefined ? match : String(replacement);
  });
}
